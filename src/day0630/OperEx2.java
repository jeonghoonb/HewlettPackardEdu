package day0630;

public class OperEx2 {

	public static void main(String[] args) {

		int x = 30;
		int y = 20;
		boolean result1 = x < y;
		boolean result2 = x > y;
		boolean result3 = x <= y;
		boolean result4 = x >= y;
		boolean result5 = x == y;
		boolean result6 = x != y;
		boolean result7 = !result1;

		System.out.println("결과 1 :" + result1);
		System.out.println("결과 2 :" + result2);
		System.out.println("결과 3 :" + result3);
		System.out.println("결과 4 :" + result4);
		System.out.println("결과 5 :" + result5);
		System.out.println("결과 6 :" + result6);
		System.out.println("결과 7 :" + result7);

	}

}
